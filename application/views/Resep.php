                                <h2>Resep Masakan Cumi Rica Rica</h2>
                                <img class="img-responsive img-centered" src="assets/img/portfolio/1.png" alt="">
                                <p>Bahan bahan dan bumbu:<br>

                                500 gram cumi segar<br>
                                ½ sdt perasan air jeruk nipis<br>
                                ¼ sdt garam halus<br>
                                5 siung bawang merah<br>
                                2 siung bawang putih<br>
                                2 buah cabai merah besar<br>
                                1 batang putih serai (iris tipis)<br>
                                2 cm jahe (digeprek)<br>
                                2 lembar daun jeruk nipis<br>
                                1 sdt saus tiram<br>
                                1 sdt saus sambal<br>
                                1 sdt saus tomat<br>
                                Air putih secukupnya<br>
                                2 sdt minyak goreng<br><br>

                                Cara membuat cumi saus padang spesial:<br><br>

                                Cumi yang sudah disiapkan kemudian dibuang tintanya, kerak-kerak kulitnya, potong bulat atau sesuai selera, lalu cuci sampai bersih dan simpan diwadah.
                                <br><br>
                                Lumuri cumi dengan perasan air jeruk nipis dan garam, aduk-aduk dan diamkan selama 10 menit.
                                Sambil menunggu siapkan cobekan lalu ulek bawang merah, bawang putih dan cabai merah sampai halus, sisihkan.<br><br>
                                Panaskan minyak goreng kemudian tumis bumbu yang sudah dihaluskan, tambahkan dengan irisan putih serai, jahe yang sudah dimemarkan, daun jeruk nipis lalu aduk-aduk dan masak sampai tercimu aroma harum.
                                Setelah bumbu harum, masukkan cumi kemudian masak sebentar sampai cumi berubah warna.<br><br>
                                Setelah itu masukkan saus tiram, saus sambal, saus tomat dan tuangkan air putih secukupnya, aduk-aduk agar bumbu tercampur rata dan masak sampai bumbu meresap dan cumi matang dengan sempurna.<br><br>
                                Angkat dan sajikan dipiring, dan cumi saus padangpun sudah siap untuk dihidangkan.</p>
