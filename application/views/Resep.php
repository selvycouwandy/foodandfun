                                <h2>Cara Membuat Cireng Goreng Isi Khas Bandung Enak & Kering</h2>
                                <img class="img-responsive img-centered" src="assets/img/portfolio/6.png" alt="">
                                <p>Bahan bahan dan bumbu:<br>

                                ¼ kg tepung sagu / tepung tapioca<br>
                                ½ sdt bawang putih halus<br>
                                2 batang daun bawang (iris tipis)<br>
                                ½ sdt bubuk kaldu ayam<br>
                                1 sdt garam halus<br>
                                ¼ sdt lada bubuk<br>
                                ½ kg minyak goreng<br>
                                Bahan biang :<br><br>

                                250 mil air putih panas<br>
                                80 gram tepung sagu / tepung tapioca<br>
                                Bahan isi cireng :<br><br>

                                3 buah cabai rawit merah<br>
                                2 siung bawang putih<br>
                                150 gram daging ayam diling<br>
                                ¼ sdm lada bubuk<br>
                                ¼ sdm gula pasir<br>
                                ½ sdm garam halus<br>
                                1 batang daun bawang (iris tipis)<br>
                                2 sdm minyak goreng<br>
                                Cara membuat cireng goreng isi khas bandung yang enak :<br><br>

                                Bahan cireng:<br>
                                Siapkan sebuah wadah kemudian bahan biang (air panas dan tepung sagu dilarutkan) aduk sampai bahan menggumpal kemudian masukkan bawang putih, irisan daun bawang, bubuk kaldu ayam, garam dan lada bubuk, aduk rata.<br>
                                Setelah itu masukkan tepung sagu secara perlahan sambil diaduk sampai adonan cireng kalis, sisihkan.<br><br>
                                Bahan isi:<br>
                                Cabai rawit dan bawang merah ditumbuk halus terlebih dulu setelah itu tumis sampai tercium aroma harum, kemudian masukkan daging ayam giling, lada bubuk, gula pasir, garam dan irisan bawang daun, aduk hingga tercampur rata lalu masak sampai daging berubah warna, angkat dan sisihkan.<br><br>
                                Penyajian:<br>
                                Ambil adonan cireng 2 sampai 3 sdm kemudian masukkan bahan isi secukupnya, bentuk adonan menjadi bulat lalu pipihkan, simpan adonan cireng isi diwadah dan lakukan sampai selesai.<br><br>
                                Setelah semua adonan cireng selesai kemudian goreng dengan minyak yang sudah panas sampai matang dengan sempurna (goreng dengan api sedang).<br><br>
                                Angkat cireng goreng isi yang sudah matang kemudian sajikan dipiring dan hidangkan dengan saus sambal atau saus bumbu rujak sebagai pelengkapnya.</p>
                                    
